class GlobalSpitial(nn.Module):
    def __init__(
        self,
        hidden_dim: int = 0,
        drop_path: float = 0,
        norm_layer: Callable[..., torch.nn.Module] = partial(nn.LayerNorm, eps=1e-6),
        # =============================
        ssm_d_state: int = 16,
        ssm_ratio: float = 2.,
        ssm_dt_rank: Any = "auto",
        ssm_act_layer=nn.SiLU,
        ssm_conv: int = 3,
        ssm_conv_bias=True,
        ssm_drop_rate: float = 0,
        ssm_init="v0",
        forward_type="v2",
        # =============================
        **kwargs,
    ):
        super().__init__()
        self.ln_1 = norm_layer(hidden_dim)
        self.self_attention = OSSM(
            d_model=hidden_dim, 
            d_state=ssm_d_state,
            ssm_ratio=ssm_ratio,
            dt_rank=ssm_dt_rank,
            act_layer=ssm_act_layer,
            # =====================
            d_conv=ssm_conv,
            conv_bias=ssm_conv_bias,
            # =====================
            dropout=ssm_drop_rate,
            initialize=ssm_init,
            forward_type=forward_type,
            )
        self.drop_path = DropPath(drop_path)
        self.pooling = nn.AdaptiveAvgPool2d(1)
        self.ln_2 = norm_layer(hidden_dim)

    def forward(self, input):

        # 先扫描垂直和水平方向
        x_hv = input.permute(0, 2, 3, 1)
        x1 = self.ln_1(x_hv)
        x1 = self.drop_path(self.self_attention(x1))
       
        # 再扫描通道方向
        x_c = self.pooling(input)
        x_c = x_c.permute(0, 2, 3, 1)
        x2 = self.ln_2(x_c)
        x2 = self.drop_path(self.self_attention(x2))
        output = x2 * x_c + x1
        output = output.permute(0, 3, 1, 2)

        return output

class MLP(nn.Module):

    def __init__(self, in_dim, hidden_dim=None, out_dim=None, act_layer=nn.GELU, drop=0.):
        super().__init__()
        out_dim = out_dim or in_dim
        hidden_dim = hidden_dim or in_dim
        self.fc1 = nn.Linear(in_dim, hidden_dim)
        self.act = act_layer()
        self.fc2 = nn.Linear(hidden_dim, out_dim)
        self.drop = nn.Dropout(drop)

    def forward(self, x):
        x = self.fc1(x)
        x = self.act(x)
        x = self.drop(x)
        x = self.fc2(x)
        x = self.drop(x)
        return x

class Global_inter_Context(nn.Module):

    def __init__(self, dim=32, out_dim=64, num_heads=2, drop=0., mlp_ratio=2.,):
        super().__init__()
        self.dim = dim
        self.num_heads = num_heads

        self.global_spitial = GlobalSpitial(hidden_dim=self.dim)
        
        self.reprojection = nn.Conv2d(dim, out_dim, kernel_size=5, stride=1, padding=2)

    def forward(self, x1):

        inter_ossm = self.global_spitial(x1)

        if self.reprojection is not None:
            attention = self.reprojection(inter_ossm)
        else:
            attention = inter_ossm  

        return attention 


class Channel_wise_Context(nn.Module):
    def __init__(self, dim, out_dim):
        super().__init__()

        self.fushion = nn.Sequential(
            nn.Conv2d(dim, 192, kernel_size=3, stride=1, padding=1),
            nn.GELU(),
            nn.Conv2d(192, 128, kernel_size=3, stride=1, padding=1),
            nn.GELU(),
            nn.Conv2d(128, out_dim, kernel_size=3, stride=1, padding=1)
        )

    def forward(self, x1):
        channel_params = self.fushion(x1)
        
        return channel_params